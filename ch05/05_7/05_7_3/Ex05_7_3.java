
class Ex05_7_3 {
    public static void main (String[] argv) {
       int a = 9, b = 10, c;

       c = a & b;
       System.out.println("測試運算元 &： "+ c);

       c = a | b;
       System.out.println("測試運算元 |： "+ c);

       c = a ^ b;
       System.out.println("測試運算元 ^： "+ c);     
     }
 }
       
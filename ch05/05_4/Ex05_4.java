
class Ex05_4 {
    public static void main (String[] argv) {
       int a;

       a = 10; 
       System.out.println("前置遞增 a = "+ ++a);

       a = 10; 
       System.out.println("後置遞增 a = "+ a++);

       a = 10; 
       System.out.println("前置遞減 a = "+ --a);

       a = 10; 
       System.out.println("後置遞減 a = "+ a--);
     }
 }
       
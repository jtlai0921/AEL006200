
class Ex05_3_2 {
    public static void main (String[] argv) {
       int a, b = 2;

       a = 15; 
       a += b;
       System.out.println("加法值 a = "+a);

       a = 15;
       a -= b;
       System.out.println("減法值 a = "+a);

       a = 15;
       a *= b;
       System.out.println("乘法值 a = "+a);

       a = 15;
       a /= b;
       System.out.println("除法值 a = "+a);

       a = 15;
       a %= b;
       System.out.println("除法值 a = "+a);
     }
 }
       
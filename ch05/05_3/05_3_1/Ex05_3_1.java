
class Ex05_3_1 {
    public static void main (String[] argv) {
       int a = 15, b = 2, c;

       c = a + b;
       System.out.println("加法值 c = "+c);

       c = a - b;
       System.out.println("減法值 c = "+c);

       c = a * b;
       System.out.println("乘法值 c = "+c);

       c = a / b;
       System.out.println("除法值 c = "+c);

       c = a % b;
       System.out.println("餘數值 c = "+c);
     }
 }
       

class Ex04_2_3 {
    public static void main (String[] argv) {
       short st;

       st = 65;
       System.out.println("一般數值 = "+st);

       st = 'A';
       System.out.println("字母A之ASCII十進位碼 = "+st);

       st = '中';
       System.out.println("中文字元十進位碼 = "+st);

       st = '\0';
       System.out.println("特殊字元之空字元碼 = "+st);
      }
 }
       